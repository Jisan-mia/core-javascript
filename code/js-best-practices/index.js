// Some javascript best practices

/*
- Meaningful variable name
- meaningful and descriptive function name
- limit the number or function arguments as less as possible
- use default argument value in function instead of conditionals
- don't need to pass flag as parameter to function, instead split function for multiple cases
- its good to create one function for one task or single task per function
- use strict equality(===) wherever possible
- don't pollute global prototype
- use conditional shorthand
- avoid using eval()
- add methods on prototype
- declare variables outside for statement
- use let/const or variable declaration
- use semicolon(;)
- use IIFE 
- use modular pattern

*/