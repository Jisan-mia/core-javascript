// Types in JavaScript

// Dynamic Typing
// You don't tell the JavaScript engine what type of data a variable holds, instead the engine figures it out while your code is running

// Variables can hold different types of values because it's all figured out during execution


// Primitive Types
// A primitive type is a type of data that represents a single value or in another word that is, not an object.

